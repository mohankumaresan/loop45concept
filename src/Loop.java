public class Loop {

    public static void main(String[] args) {
        for (int i = 9; i <= 37; i += 6) {

            System.out.println("i value is" + i);
        }

        for (int i = 67; i >= 12; i -= 4) {

            System.out.println("i value is" + i);
        }

        for (int i = 1; i <= 10; i++) {

            System.out.println("i value is" + i*6);
        }


    }
}
